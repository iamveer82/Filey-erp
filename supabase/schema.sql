-- ---------- integrity: non-negative money/quantity checks ----------
-- Added NOT VALID so they enforce new/updated rows without failing on any
-- pre-existing data; validate later once data is known clean.
do $$
declare
  c record;
  checks jsonb := jsonb_build_array(
    jsonb_build_object('t','products','n','chk_products_nonneg','e','unit_price >= 0 and cost_price >= 0 and quantity >= 0 and reorder_level >= 0'),
    jsonb_build_object('t','orders','n','chk_orders_nonneg','e','total >= 0'),
    jsonb_build_object('t','order_items','n','chk_order_items_nonneg','e','quantity >= 0 and unit_price >= 0'),
    jsonb_build_object('t','invoice_docs','n','chk_invoice_docs_nonneg','e','tax_rate >= 0 and discount >= 0'),
    jsonb_build_object('t','invoice_doc_items','n','chk_invoice_items_nonneg','e','qty >= 0 and unit_price >= 0'),
    jsonb_build_object('t','quotation_items','n','chk_quote_items_nonneg','e','qty >= 0 and rate >= 0'),
    jsonb_build_object('t','expenses','n','chk_expenses_nonneg','e','amount >= 0'),
    jsonb_build_object('t','transactions','n','chk_txn_nonneg','e','amount >= 0'),
    jsonb_build_object('t','payroll','n','chk_payroll_nonneg','e','basic >= 0 and allowances >= 0 and deductions >= 0'),
    jsonb_build_object('t','purchase_order_items','n','chk_po_items_nonneg','e','quantity >= 0 and unit_cost >= 0')
  );
begin
  for c in select * from jsonb_to_recordset(checks) as x(t text, n text, e text) loop
    if not exists (select 1 from pg_constraint where conname = c.n) then
      execute format('alter table public.%I add constraint %I check (%s) not valid;', c.t, c.n, c.e);
    end if;
  end loop;
end $$;

-- ---------- audit log: auto-record create/update/delete ----------
-- Populates audit_log for key entities with the acting user + entity ref.
-- security definer so it can write the row regardless of the caller's RLS;
-- failures never block the underlying operation.
create or replace function public.log_audit()
returns trigger language plpgsql security definer set search_path = public as $$
declare
  actor_name text;
  rid text;
begin
  begin
    select coalesce(name, email, 'system') into actor_name
      from public.profiles where id = auth.uid();
    if (TG_OP = 'DELETE') then rid := old.id::text; else rid := new.id::text; end if;
    insert into public.audit_log (actor, action, entity, details, org_id, user_id)
    values (
      coalesce(actor_name, 'system'),
      lower(TG_OP),
      TG_TABLE_NAME,
      TG_TABLE_NAME || ' #' || coalesce(rid, '?'),
      public.current_org(),
      auth.uid()
    );
  exception when others then
    null; -- never let auditing break the real write
  end;
  if (TG_OP = 'DELETE') then return old; else return new; end if;
end $$;

do $$
declare
  t text;
  audited text[] := array[
    'products','orders','invoice_docs','quotations','crm_customers',
    'crm_leads','crm_opportunities','expenses','suppliers',
    'purchase_orders','employees','payroll'
  ];
begin
  foreach t in array audited loop
    execute format('drop trigger if exists %I on public.%I;', 'trg_audit_'||t, t);
    execute format(
      'create trigger %I after insert or update or delete on public.%I '
      || 'for each row execute function public.log_audit();',
      'trg_audit_'||t, t
    );
  end loop;
end $$;

