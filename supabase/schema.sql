-- Caller's active organization key (text). Defaults to 'default' so any
-- pre-existing single-user data keeps working unchanged after migration.
create or replace function public.current_org()
returns text language sql stable security definer set search_path = public as $$
  select coalesce(
    (select org_id from public.profiles where id = auth.uid()),
    'default'
  )
$$;

-- True when the caller is an owner/admin of their active org.
-- security definer avoids RLS recursion on org_members.
create or replace function public.is_org_admin()
returns boolean language sql stable security definer set search_path = public as $$
  select exists (
    select 1 from public.org_members
    where org_id = public.current_org()
      and user_id = auth.uid()
      and role in ('owner', 'admin')
  )
$$;

