alter table profiles add column if not exists language text default 'English (US)';
alter table profiles add column if not exists timezone text;
alter table profiles add column if not exists date_format text;
alter table profiles add column if not exists time_format text;

alter table profiles enable row level security;
-- SECURITY: a user may read/insert/delete only their own profile, and may
-- only switch their active org_id to an organization they actually belong
-- to. The old "for all" policy let a user set org_id to ANY org, which made
-- current_org() return that org and exposed its shared data.
drop policy if exists profiles_owner on profiles;
drop policy if exists profiles_self_select on profiles;
drop policy if exists profiles_self_insert on profiles;
drop policy if exists profiles_self_update on profiles;
drop policy if exists profiles_self_delete on profiles;

create policy profiles_self_select on profiles for select
  using (id = auth.uid());
create policy profiles_self_insert on profiles for insert
  with check (id = auth.uid());
create policy profiles_self_update on profiles for update
  using (id = auth.uid())
  with check (
    id = auth.uid()
    and (
      org_id = 'default'
      or exists (
        select 1 from public.org_members m
        where m.org_id = profiles.org_id and m.user_id = auth.uid()
      )
    )
  );
create policy profiles_self_delete on profiles for delete
  using (id = auth.uid());

-- ---------- shared updated_at trigger fn ----------
create or replace function set_updated_at()
returns trigger language plpgsql as $$
begin
  new.updated_at = now();
  return new;
end $$;

drop trigger if exists trg_profiles_updated on profiles;
create trigger trg_profiles_updated before update on profiles
  for each row execute function set_updated_at();

