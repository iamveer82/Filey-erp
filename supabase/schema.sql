-- ---------- RLS + org scoping + updated_at trigger for every table ----------
do $$
declare
  t text;
  tables text[] := array[
    'products','orders','order_items','invoices',
    'employees','attendance','payroll',
    'accounts','expenses','transactions',
    'app_users','app_settings','audit_log',
    'crm_leads','crm_customers','crm_opportunities','crm_activities',
    'company_profile','invoice_docs','invoice_doc_items',
    'quotations','quotation_items','quotation_templates','tool_runs',
    'suppliers','purchase_orders','purchase_order_items'
  ];
begin
  foreach t in array tables loop
    execute format(
      'alter table %I add column if not exists org_id text not null default public.current_org();',
      t
    );
    execute format('alter table %I enable row level security;', t);
    execute format('drop policy if exists %I on %I;', t || '_owner', t);
    execute format('drop policy if exists %I on %I;', t || '_org', t);
    execute format(
      'create policy %I on %I for all using (org_id = public.current_org()) with check (org_id = public.current_org());',
      t || '_org', t
    );
    execute format('drop trigger if exists %I on %I;', 'trg_' || t || '_updated', t);
    execute format(
      'create trigger %I before update on %I for each row execute function set_updated_at();',
      'trg_' || t || '_updated', t
    );
  end loop;
end $$;

-- ---------- auto-provision a personal org + profile on signup ----------
create or replace function handle_new_user()
returns trigger language plpgsql security definer set search_path = public as $$
declare
  new_org uuid;
begin
  insert into public.organizations (id, name, owner_id)
  values (gen_random_uuid(), coalesce(new.email, 'My Organization'), new.id)
  returning id into new_org;

  insert into public.profiles (id, email, name, org_id)
  values (
    new.id,
    coalesce(new.email, ''),
    coalesce(new.raw_user_meta_data->>'full_name', 'User'),
    new_org::text
  )
  on conflict (id) do nothing;

  insert into public.org_members (org_id, user_id, role)
  values (new_org::text, new.id, 'owner')
  on conflict (org_id, user_id) do nothing;

  return new;
end $$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function handle_new_user();

-- ---------- realtime: broadcast row changes for live multi-client sync ----------
-- Every table the app reads is added to the supabase_realtime publication
-- so changes from any client (desktop, web, or a direct DB edit) stream to
-- open sessions. RLS still filters the feed: a client only receives changes
-- for rows it may read. Idempotent — safe to re-run.
do $$
declare
  t text;
  rt_tables text[] := array[
    'products','orders','order_items','invoices',
    'employees','attendance','payroll',
    'accounts','expenses','transactions',
    'app_users','app_settings','audit_log',
    'crm_leads','crm_customers','crm_opportunities','crm_activities',
    'company_profile','invoice_docs','invoice_doc_items',
    'quotations','quotation_items','quotation_templates','tool_runs',
    'suppliers','purchase_orders','purchase_order_items',
    'organizations','org_members','profiles'
  ];
begin
  if not exists (
    select 1 from pg_publication where pubname = 'supabase_realtime'
  ) then
    create publication supabase_realtime;
  end if;
  foreach t in array rt_tables loop
    if not exists (
      select 1 from pg_publication_tables
      where pubname = 'supabase_realtime'
        and schemaname = 'public'
        and tablename = t
    ) then
      execute format(
        'alter publication supabase_realtime add table public.%I;', t
      );
    end if;
  end loop;
end $$;

