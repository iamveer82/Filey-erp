create or replace function public.accept_invitation(invite uuid)
returns void language plpgsql security definer set search_path = public as $$
declare
  inv public.invitations;
  myemail text;
begin
  select email into myemail from public.profiles where id = auth.uid();
  select * into inv from public.invitations
    where id = invite and status = 'pending';
  if inv.id is null then
    raise exception 'Invitation not found or already used';
  end if;
  if lower(inv.email) <> lower(coalesce(myemail, '')) then
    raise exception 'This invitation is for a different email address';
  end if;
  insert into public.org_members (org_id, user_id, role, modules)
  values (inv.org_id, auth.uid(), inv.role, inv.modules)
  on conflict (org_id, user_id)
    do update set role = excluded.role, modules = excluded.modules;
  update public.profiles set org_id = inv.org_id where id = auth.uid();
  update public.invitations set status = 'accepted', updated_at = now()
    where id = invite;
end $$;
grant execute on function public.accept_invitation(uuid) to authenticated;

-- ---------- RLS + org scoping + privacy model + updated_at trigger ----------
-- Privacy model (per the product spec):
--   * A member's records are PRIVATE to them by default.
--   * Org owners/admins can see and manage everything in the org.
--   * A record becomes visible to the whole org only when its owner sets
--     `shared = true` (opt-in, per record). Shared records are read-only
--     to other members (only the owner or an admin can edit them).
-- Org-level tables (settings, audit, company profile, legacy app_users)
-- stay org-scoped rather than per-member.
do $$
declare
  t text;
  tables text[] := array[
    'products','orders','order_items',
    'employees','attendance','payroll',
    'accounts','expenses','transactions',
    'app_users','app_settings','audit_log',
    'crm_leads','crm_customers','crm_opportunities','crm_activities',
    'company_profile','invoice_docs','invoice_doc_items',
    'quotations','quotation_items','quotation_templates','tool_runs',
    'suppliers','purchase_orders','purchase_order_items'
  ];
  org_only text[] := array[
    'company_profile','app_settings','audit_log','app_users'
  ];
begin
  foreach t in array tables loop
    execute format(
      'alter table %I add column if not exists org_id text not null default public.current_org();',
      t
    );
    execute format('alter table %I enable row level security;', t);
    execute format('drop trigger if exists %I on %I;', 'trg_' || t || '_updated', t);
    execute format(
      'create trigger %I before update on %I for each row execute function set_updated_at();',
      'trg_' || t || '_updated', t
    );

    -- clear any prior policy variants
    execute format('drop policy if exists %I on %I;', t || '_owner', t);
    execute format('drop policy if exists %I on %I;', t || '_org', t);
    execute format('drop policy if exists %I on %I;', t || '_access', t);

    if t = any(org_only) then
      execute format(
        'create policy %I on %I for all using (org_id = public.current_org()) with check (org_id = public.current_org());',
        t || '_org', t
      );
    else
      execute format(
        'alter table %I add column if not exists shared boolean not null default false;',
        t
      );
      -- SECURITY: the admin / shared branches MUST be scoped to the
      -- caller's own org. is_org_admin() is true for an admin of *their*
      -- org; without the org_id match it would grant access to every
      -- org's rows (cross-tenant leak). Always require org_id = current_org
      -- on anything that isn't the caller's own row.
      execute format(
        'create policy %I on %I for all '
        || 'using (user_id = auth.uid() '
        || 'or (org_id = public.current_org() '
        || 'and (public.is_org_admin() or shared = true))) '
        || 'with check (user_id = auth.uid() '
        || 'or (org_id = public.current_org() and public.is_org_admin()));',
        t || '_access', t
      );
    end if;
  end loop;
end $$;

-- ---------- backfill org_id from each row owner's profile ----------
-- When org_id was first added, the default public.current_org() ran with a
-- null auth.uid() (admin/SQL context) and resolved to 'default' for every
-- existing row. That orphans pre-migration rows in org 'default' while a
-- signed-in user's current_org() is their real org UUID, so RLS blocks
-- updates. Re-point each row to its owner's actual org. Idempotent.
do $$
declare
  t text;
  tables text[] := array[
    'products','orders','order_items',
    'employees','attendance','payroll',
    'accounts','expenses','transactions',
    'app_users','app_settings','audit_log',
    'crm_leads','crm_customers','crm_opportunities','crm_activities',
    'company_profile','invoice_docs','invoice_doc_items',
    'quotations','quotation_items','quotation_templates','tool_runs',
    'suppliers','purchase_orders','purchase_order_items'
  ];
begin
  foreach t in array tables loop
    execute format(
      'update public.%I x set org_id = p.org_id
         from public.profiles p
        where x.user_id = p.id
          and x.org_id is distinct from p.org_id;',
      t
    );
  end loop;
end $$;

-- ---------- company_profile: one shared profile per organization ----------
-- The company identity belongs to the org, not each member. Keep a single
-- row per org, let any member read it, but only org admins (owner/admin)
-- may change it — so invited members cannot rename the company.
do $$
begin
  -- Collapse any duplicates, keeping the earliest row per org.
  delete from public.company_profile a
   using public.company_profile b
   where a.org_id = b.org_id and a.id > b.id;

  if exists (
    select 1 from pg_constraint where conname = 'company_profile_user_id_key'
  ) then
    alter table public.company_profile drop constraint company_profile_user_id_key;
  end if;

  if not exists (
    select 1 from pg_constraint where conname = 'company_profile_org_id_key'
  ) then
    alter table public.company_profile
      add constraint company_profile_org_id_key unique (org_id);
  end if;
end $$;

-- Read for all org members; write for org admins only.
drop policy if exists company_profile_org on company_profile;
drop policy if exists company_profile_read on company_profile;
drop policy if exists company_profile_write on company_profile;
create policy company_profile_read on company_profile for select
  using (org_id = public.current_org());
create policy company_profile_write on company_profile for all
  using (org_id = public.current_org() and public.is_org_admin())
  with check (org_id = public.current_org() and public.is_org_admin());

-- ---------- auto-provision a personal org + profile on signup ----------
create or replace function handle_new_user()
returns trigger language plpgsql security definer set search_path = public as $$
declare
  new_org uuid;
begin
  insert into public.organizations (id, name, owner_id)
  values (gen_random_uuid(), coalesce(new.email, 'My Organization'), new.id)
  returning id into new_org;

  insert into public.profiles (id, email, name, org_id)
  values (
    new.id,
    coalesce(new.email, ''),
    coalesce(new.raw_user_meta_data->>'full_name', 'User'),
    new_org::text
  )
  on conflict (id) do nothing;

  insert into public.org_members (org_id, user_id, role)
  values (new_org::text, new.id, 'owner')
  on conflict (org_id, user_id) do nothing;

  return new;
end $$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function handle_new_user();

-- ---------- realtime: broadcast row changes for live multi-client sync ----------
-- Every table the app reads is added to the supabase_realtime publication
-- so changes from any client (desktop, web, or a direct DB edit) stream to
-- open sessions. RLS still filters the feed: a client only receives changes
-- for rows it may read. Idempotent — safe to re-run.
do $$
declare
  t text;
  rt_tables text[] := array[
    'products','orders','order_items',
    'employees','attendance','payroll',
    'accounts','expenses','transactions',
    'app_users','app_settings','audit_log',
    'crm_leads','crm_customers','crm_opportunities','crm_activities',
    'company_profile','invoice_docs','invoice_doc_items',
    'quotations','quotation_items','quotation_templates','tool_runs',
    'suppliers','purchase_orders','purchase_order_items',
    'organizations','org_members','profiles','invitations','org_messages'
  ];
begin
  if not exists (
    select 1 from pg_publication where pubname = 'supabase_realtime'
  ) then
    create publication supabase_realtime;
  end if;
  foreach t in array rt_tables loop
    if not exists (
      select 1 from pg_publication_tables
      where pubname = 'supabase_realtime'
        and schemaname = 'public'
        and tablename = t
    ) then
      execute format(
        'alter publication supabase_realtime add table public.%I;', t
      );
    end if;
  end loop;
end $$;

